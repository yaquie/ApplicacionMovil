package express.deli.cosema.deliexpressapp.platos.entidad;

public class PlatosUi {
    private String idPlatos;
    private String nombrePlato;
    private String descripcionPlato;
    private String imagenPlato;
    private String precioPersonalPlato;
    private String precioMedianoPlato;
    private String precioFamiliarPlato;
    private String tipoPlato;
    private String tipoCategoria;

    public PlatosUi() {
    }

    public String getIdPlatos() {
        return idPlatos;
    }

    public void setIdPlatos(String idPlatos) {
        this.idPlatos = idPlatos;
    }

    public String getNombrePlato() {
        return nombrePlato;
    }

    public void setNombrePlato(String nombrePlato) {
        this.nombrePlato = nombrePlato;
    }

    public String getImagenPlato() {
        return imagenPlato;
    }

    public void setImagenPlato(String imagenPlato) {
        this.imagenPlato = imagenPlato;
    }

    public String getPrecioPersonalPlato() {
        return precioPersonalPlato;
    }

    public void setPrecioPersonalPlato(String precioPersonalPlato) {
        this.precioPersonalPlato = precioPersonalPlato;
    }

    public String getPrecioMedianoPlato() {
        return precioMedianoPlato;
    }

    public void setPrecioMedianoPlato(String precioMedianoPlato) {
        this.precioMedianoPlato = precioMedianoPlato;
    }

    public String getPrecioFamiliarPlato() {
        return precioFamiliarPlato;
    }

    public void setPrecioFamiliarPlato(String precioFamiliarPlato) {
        this.precioFamiliarPlato = precioFamiliarPlato;
    }

    public String getTipoPlato() {
        return tipoPlato;
    }

    public void setTipoPlato(String tipoPlato) {
        this.tipoPlato = tipoPlato;
    }

    public String getTipoCategoria() {
        return tipoCategoria;
    }

    public void setTipoCategoria(String tipoCategoria) {
        this.tipoCategoria = tipoCategoria;
    }

    public String getDescripcionPlato() {
        return descripcionPlato;
    }

    public void setDescripcionPlato(String descripcionPlato) {
        this.descripcionPlato = descripcionPlato;
    }
}
